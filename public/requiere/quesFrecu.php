        <h5 class="ToolsTic_Verde-text">¿Qué es ToolsTIC?</h5>
        <p>
            ToolsTIC es una estrategia educativa para el aprendizaje de competencias en Lenguaje y Herramientas
            Informáticas de la Universidad de Nariño. Dicha estrategia está conformada por la prueba de homologación en
            términos de competencias, el curso de Lenguaje y Herramientas Informáticas bajo la modalidad B-Learning y un
            Open Course Ware (Curso abierto de aprendizaje) de acceso libre y sin restricciones al conocimiento, que
            ejercite las competencias básicas del curso de Lenguaje y Herramientas Informáticas.
        </p>

        <h5 class="ToolsTic_Verde-text">¿Qué es el Open Course ToolsTIC?</h5>
        <p>
            El Open Course ToolsTIC es ofrecido con el objetivo de facilitar a los estudiantes de la universidad, la
            presentación de la prueba de homologación del curso de competencias en Lenguaje y Herramientas Informáticas
            y de brindar conocimientos y contenidos de calidad a toda la población en general. Es un curso abierto a
            todo el público, donde podrás encontrar diverso material lo único que necesitas tener es acceso a una
            computadora o dispositivo móvil con un navegador web como Mozilla Firefox o Google Chrome, una conexión a
            internet y muchas ganas de aprender.
        </p>


        <h5 class="ToolsTic_Verde-text">¿Qué es Homologa ToolsTIC?</h5>
        <p>
            Homologa ToolsTIC es la prueba de homologación propuesta en el artículo 3 del Acuerdo No. 032 del 25 de
            abril de 2017 del Consejo Académico De La Universidad de Nariño; donde se afirma: “[...] diseñe, aplique y
            evalúe la prueba diagnóstica de Lenguaje y Herramientas Informáticas a los estudiantes que ingresan a primer
            semestre en los respectivos programas académicos [...]”. Prueba que debe realizarse de forma obligatoria y
            antes de tercer semestre académico del estudiante.
        </p>

        <h5 class="ToolsTic_Verde-text">¿Qué es el curso ToolsTIC?</h5>
        <p>
            El curso ToolsTIC es un sistema de gestión de aprendizaje, que permite administrar, distribuir, monitorear,
            evaluar y apoyar las diferentes actividades previamente diseñadas y programadas dentro del proceso de
            formación del módulo de Lenguaje y Herramientas Informáticas de la Universidad de Nariño.
        </p>


